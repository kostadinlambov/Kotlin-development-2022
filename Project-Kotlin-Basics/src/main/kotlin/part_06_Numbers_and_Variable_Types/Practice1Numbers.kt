package part_06_Numbers_and_Variable_Types

fun main() {
//    Create an int variable and print it.
//    Confirm by printing its class.
    val numInt = 5
    println(numInt)
    println(numInt::class.java)

//    Create a long variable and print it.
//    Confirm by printing its class.
    val numLong = 5000000000
    println(numLong)
    println(numLong::class.java)

//    Create a double variable and print it.
//    Confirm by printing its class.
    val numDouble = 5.000000023
    println(numDouble)
    println(numDouble::class.java)


}